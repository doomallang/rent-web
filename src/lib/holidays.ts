export function isHoliday(date: Date): boolean {
  const key = date.toISOString().slice(0, 10);
  return HOLIDAYS.includes(key);
}

export function isWeekend(date: Date): boolean {
  const day = date.getDay();
  return day === 0 || day === 6;
}

